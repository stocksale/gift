					<p>
El sistema de riego por goteo está diseñado para instalarse en una cama de jardín o en un invernadero de su jardín. Se conecta a cualquier barril que tengas. Se vierte con agua tibia calentada en un barril al sol. Los emisores (goteros) se colocan debajo de cada planta, o uniformemente sobre el lecho, los emisores hacia arriba.</p>
